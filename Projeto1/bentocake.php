<h2>Bentô Cake</h2>
<p>Bentô Cake é a sensação do momento e é uma opção muito divertida para aqueles casais bem humorados, com frases e imagens com muita personalidade!</p>
<h3>Massa</h3>
<h4>Ingredientes:</h4>
<ul>
    <li>100 g de manteiga</li>
    <li>2 xícaras de chá de açúcar refinado</li>
    <li>4 ovos</li>
    <li>1 colher de chá de emulsão saborizante de baunilha</li>
    <li>1 xícara de chá de leite ou suco</li>
    <li>3 xícaras de chá de farinha de trigo</li>
    <li>1 colher de sopa de fermento em pó</li>
    <li>1 pitada de sal</li>
</ul>
<p><strong>Modo de preparo:</strong> Bater o açúcar com a manteiga até ficar bem clarinho, acrescentar os ovos e bater mais um pouco, em seguida intercalar a farinha com o leite. Por último o sal e o fermento.</p><br>
<p>Colocar em formas de 10,15, ou 20 cm untada com desmoldante. Levar ao forno 180 graus por mais ou menos 25 minutos.</p>
<h3>Mousse de Chocolate</h3>
<h4>Ingredientes:</h4>
<ul>
    <li>350 g de chocolate derretido</li>
    <li>1 caixa de creme de leite</li>
    <li>200 g de chantilly batido</li>
    <li>1 colher de sopa de rum, conhaque ou licor de amarula</li>
</ul>
<p><strong>Modo de preparo:</strong> Derreter o chocolate com o creme de leite no microondas, acrescentar o chantilly batido e a bebida.</p>
<h3>Geleia de Morango</h3>
<h4>Ingredientes:</h4>
<ul>
    <li>500 g de morangos picados</li>
    <li>1 xícara e meia de açúcar</li>
    <li>1 colher chá de ácido cítrico</li>
    <li>1 colher (chá) de pectina</li>
    <li>1 colher de chá de emulsão de frutas vermelhas</li>
</ul>
<p><strong>Modo de preparo:</strong> Em uma panela coloque os morangos picados e o açúcar e a pectina, mexa e deixe cozinhar em fogo baixo por cerca de 15 minutos até dar uma engrossada e desmanchar um pouco os pedaços de morango. Acrescente o suco, ácido cítrico e a emulsão misture e reserve.</p>