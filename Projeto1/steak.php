<h2>Steak de Frango ao molho Indiano</h2>
<p>Bentô Cake é a sensação do momento e é uma opção muito divertida para aqueles casais bem humorados, com frases e imagens com muita personalidade!</p>
<h3>Frango</h3>
<h4>Ingredientes:</h4>
<ul>
    <li>500g de filés de peito de frango</li>
    <li>150g de yogurte natural</li>
    <li>1 colher (chá) de garam masala ou curry</li>
    <li>½ colher (café) de cominho</li>
    <li>½ colher (café) de semente de coentro, triturada</li>
    <li>¼ colher (café) de pimenta cayenna</li>
    <li>1 colher (sp) de extrato de tomate</li>
    <li>1 dente de alho amassado</li>
    <li>2cm de gengibre fresco descascado e ralado</li>
    <li>1 colher (café) de casca de limão, ralado</li>
    <li>Suco de 1 limão.</li>
    <li>2 colheres (sp) de folhas de coentro picado</li>
</ul>
<p><strong>Modo de preparo:</strong> BCorte os filés de frango em bifes e parta-os em tiras mais largas.
Prepare uma marinada batendo todos os outros ingredientes no processador de alimentos. Junte as tiras de frango , misture, cubra e gele por 6 horas ou de véspera.</p><br>
<p>Retire as tiras de frango da salmoura, escorra , fixe em espetinhos de madeira que foram deixados em água gelada por 30 minutos, formando ondas. Coloque em assadeira e leve ao forno quente para dourar.Vire na metade do tempo.</p>
<h3>Molho</h3>
<h4>Ingredientes:</h4>
<ul>
    <li>250ml de yogurte natural integral</li>
    <li>2 colheres (sp) de creme de leite</li>
    <li>1 pepino (japonês) descascado, sem sementes em cubinhos</li>
    <li>2 colheres (sp) de folhas frescas de coentro, picadas</li>
    <li>Sal</li>
    <li>Pimenta-do-reino, moída na hora</li>
</ul>
<p><strong>Modo de preparo:</strong> Em um bowl, misture todos os ingredientes, cubra e gele até o momento de servir.</p>
