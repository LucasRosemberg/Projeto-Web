<h2>Bolo Bem Casado</h2>
<p>Bentô Cake é a sensação do momento e é uma opção muito divertida para aqueles casais bem humorados, com frases e imagens com muita personalidade!</p>
<h3>Massa</h3>
<h4>Ingredientes:</h4>
<ul>
    <li>7 ovos</li>
    <li>350g de açúcar (2 1/3 xícaras chá)</li>
    <li>150ml de leite (3/4 xícara chá)</li>
    <li>360g de Farinha de Trigo (3 1/3 xícara chá)</li>
    <li>10g de fermento em pó</li>
</ul>
<p><strong>Modo de preparo:</strong> Mistura-se na batedeira as claras, as gemas, o açúcar, a água e por último a Farinha de Trigo e o fermento misturando bem. Ligue a batedeira em velocidade máxima de 3 a 4 minutos, unte a forma e leve ao forno a 190ºC.</p>
<h3>Recheio</h3>
<h4>Ingredientes:</h4>
<ul>
    <li>800g de leite condensado ( 2 latas)</li>
    <li>4 gemas peneiradas</li>
    <li>1 1/2 colher de manteiga sem sal</li>
</ul>
<p><strong>Modo de preparo:</strong> DMisture em uma caçarola estes ingredientes e mexa até o ponto de bala mole. Divida o pão-de-ló de Farinha de Trigo em três partes, recheie duas vezes.</p>
<h3>Cobertura</h3>
<p><strong>Modo de preparo:</strong> Polvilhe com açúcar de confeiteiro, aplique um belo arranjo de flores com um laço de fita, ou frutas nevadas.</p>